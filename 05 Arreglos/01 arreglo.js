// Declaración de un arreglo vacío
let miArreglo = [];

// Agregar elementos al arreglo
miArreglo.push("elemento1");
miArreglo.push("elemento2");
miArreglo.push("elemento3");

// Acceder a elementos del arreglo
console.log(miArreglo[0]); // Imprime "elemento1"
console.log(miArreglo[2]); // Imprime "elemento3"

// Modificar elementos del arreglo
miArreglo[1] = "nuevoElemento";

// Eliminar elementos del arreglo
miArreglo.pop(); // Elimina el último elemento del arreglo

// Agregar elementos al arreglo
miArreglo.push("elemento3"); // Agrega un elemento al final del arreglo

// Obtener la longitud del arreglo
console.log(miArreglo.length); // Imprime 2

console.log(miArreglo); // Imprime ["elemento1", "nuevoElemento"]

