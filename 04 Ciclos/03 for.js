
// Ejemplo de bucle for
let iniciador = 0;
let finalizador = 100;

for(iniciador; iniciador <= finalizador && iniciador >= 0; iniciador++){
    console.log(iniciador);
}


// Ejemplo de bucle pero en while
iniciador = 0;

while(iniciador <= finalizador && iniciador >= 0){
    console.log(iniciador);
    iniciador++;
}
